package com.alphaomardiallo.mareu.models;

public class Collaborator {
    String mCollaboratorLastName;
    String mCollaboratorFirstName;
    String mCollaboratorEmail;

    public Collaborator(String collaboratorLastName, String collaboratorFirstName, String collaboratorEmail) {
        mCollaboratorLastName = collaboratorLastName;
        mCollaboratorFirstName = collaboratorFirstName;
        mCollaboratorEmail = collaboratorEmail;
    }
}
